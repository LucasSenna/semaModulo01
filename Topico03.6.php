                <h2 class="font-bold">3.6.2 - Conselho Municipal de Meio Ambiente (Comdema ou Comam)</h2>
                <hr>

                <p class="Texto">
                    O COMDEMA é um órgão colegiado, isto é, formado por secretarias municipais,
                    sindicatos, ONGs, associação comercial e outros. Tem o objetivo de assessorar o(a)
                    Prefeito(a) em questões relacionadas ao equilíbrio ambiental e à melhoria da qualidade de
                    vida local. A principal finalidade do COMDEMA é unir esforços entre a sociedade civil
                    organizada e o poder público na defesa conjunta do Meio Ambiente.
                </p>
                <br>

                <p class="Texto">
                    Os COMDEMAS têm respaldo legal na Política Nacional do Meio Ambiente (Lei Nº
                    6.938/81) e são espaços de construção coletiva, nos quais se podem exercer o direito de
                    cidadania e, ao mesmo tempo, o dever constitucional de defender e preservar o Meio
                    Ambiente para as presentes e futuras gerações.
                </p>
                <br>

                <p class="Texto">
                    O COMDEMA pode receber denúncias sobre crimes ambientais e procurar resolver, a
                    nível do município ou até procurar a SEMA/SEMACE.
                </p>
                <br>

